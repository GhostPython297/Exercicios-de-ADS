# 1. Função Simples: Multiplicação
# Enunciado:
# Crie uma função chamada multiplicacao que receba cinco números como parâmetros e retorne
# a multiplicação desses números. Em seguida, chame a função e imprima o resultado.

def multiplicacao(n1, n2, n3, n4, n5):
    return n1 * n2 * n3 * n4 * n5

print(multiplicacao(1, 2, 3, 4, 5))