# 9. Lógica de Potência
# Questão: Crie uma função chamada potencia que receba dois parâmetros, base e expoente,
# e retorne o resultado da base elevada ao expoente.
# Dica: Você pode usar o operador ** ou a função pow().

def potencia(base, expoente):
    return base ** expoente

print(potencia(2, 3))